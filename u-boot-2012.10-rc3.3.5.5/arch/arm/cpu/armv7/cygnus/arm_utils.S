/*-----------------------------------------------------------------------------------------*/

.global get_pc
get_pc:
    mov r0,pc
    bx lr
/*-----------------------------------------------------------------------------------------*/

.global get_sp
get_sp:
    mov r0,sp
    bx lr
/*-----------------------------------------------------------------------------------------*/

.global get_cpsr
get_cpsr:
    mrs r0,cpsr
    bx lr
/*-----------------------------------------------------------------------------------------*/
